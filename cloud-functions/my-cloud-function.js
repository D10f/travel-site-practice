module.handler = function(event, context, callback){

  if(event.httpMethod !== 'POST'){
    return callback(null, {
      statusCode: 401,
      body: 'You sir, are not authorized'
    });
  }

  return callback(null, {
    statusCode: 200,
    body: {
      title: 'Welcome to the amazing world of React and Cloud Functions!',
      body: 'This text was generated by Netlify Cloud Functions, and delivered to you by React'
    }
  })
};
